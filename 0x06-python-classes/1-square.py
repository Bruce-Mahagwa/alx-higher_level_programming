"""Square class definition"""

class Square:
    """A square with a size attribute

    Atributes:
    __size(int): size of side of a square
    """
    def __init__(self, size):
        """Initializes the data

        Args:
        size(int): size of the side of a square

        Returns: None
        """
        self.__size = size

